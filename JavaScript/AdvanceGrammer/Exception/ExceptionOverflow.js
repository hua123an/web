/*
* Error对象属性：
* 属性                    描述
* name                  设置或返回错误名
* message               设置或返回错误信息(一条字符串)
* */
/*
* Error Name Values
* error的name属性可返回六个不同的值：
* 错误名                   描述
* EvalError               已在eval()函数中发生的错误
* RangeError              已发生超出数字范围的错误
* ReferenceError          已发生非法引用
*SyntaxError              已发生语法错误
*TypeError                已发生类型错误
* URLError                在encodeURL中发生错误
* */